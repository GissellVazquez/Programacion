#Vamos a imprimir los numeros del 1 a 5 utilizando un ciclo for 
#Definimos un rango de numeros del 1 al 6(el ultimo numero no esta incluido)
#Esto significa que el ciclo se ejecutara 5 veces (para los valores 1,2,3,4 y 5)
for numero in range (1,6):
    #Imprimimos el valor actual de "numero"
    print(f"Numero actual: {numero} ")
#Despues de que el ciclo for termin, mostramos un mensaje de despedida.
print("!Ciclo for completado!")